    private void populateLabAdminPanel() {
            DefaultTableModel model = (DefaultTableModel) tblLabTestList.getModel();         
         model.setRowCount(0);
        
        for(Patient p : ecosystem.getPatientList().getPatientList())
        {
           for(Encounter e : p.getEncounterHistory())
           {
                        Object[] row = new Object[4];
                        row[0]=p;
                        row[1]=e.getTreatment().getLaboratoryList().get(0).getLabTest();
                        row[2]=e.getTreatment().getLaboratoryStatus();
                        row[3]=e;
                     model.addRow(row);
       
           }
            
        }
    }
