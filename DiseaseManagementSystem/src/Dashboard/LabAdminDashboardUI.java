    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int selectedRowIndex = tblLabTestList.getSelectedRow();

        if(selectedRowIndex < 0)
        {
            JOptionPane.showMessageDialog(this, "Please Select a row of Encounter table");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblLabTestList.getModel();
        Encounter e = (Encounter) model.getValueAt(selectedRowIndex, 3);

        Patient p = (Patient) model.getValueAt(selectedRowIndex, 0);

        System.out.println("Hello" + p.getName());

        if(p.getInsuranceRequest().equals("Approved") && (p.getCoverAmount()>= Integer.parseInt(txtLabAdminAmountToPay.getText())))
        {
            if(p.getCoverAmount()>= Integer.parseInt(txtLabAdminAmountToPay.getText()))
            {

                if(e.getTreatment().getLaboratoryStatus().equalsIgnoreCase("Pending"))
                {
                    p.setCoverAmount(p.getCoverAmount()- Integer.parseInt(txtLabAdminAmountToPay.getText()));
                    e.getTreatment().setLaboratoryStatus("Completed");
                    JOptionPane.showMessageDialog(this, "Laboratory Request is Completed");

                }

                else{

                    JOptionPane.showMessageDialog(this, "Laboratory Request is Already Complete");

                }

            }

        }

        else if((p.getAccountBal() >= Integer.parseInt(txtLabAdminAmountToPay.getText())))
        {
            if(e.getTreatment().getLaboratoryStatus().equalsIgnoreCase("Pending"))
            {
                p.setAccountBal(p.getAccountBal()- Integer.parseInt(txtLabAdminAmountToPay.getText()));
                e.getTreatment().setLaboratoryStatus("Completed");
                JOptionPane.showMessageDialog(this, "Laboratory Request is  Completed");

            }

            else{

                JOptionPane.showMessageDialog(this, "Laboratory Request is Already Completed");

            }

        }

        else {
            JOptionPane.showMessageDialog(this, "Patient doesn't have enough balance in account.");

        }

        populateLabAdminPanel();
        txtLabAdminAmountToPay.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRowIndex = tblLabTestList.getSelectedRow();

        if(selectedRowIndex < 0)
        {
            JOptionPane.showMessageDialog(this, "Please Select a row of Encounter table");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblLabTestList.getModel();
        Encounter e = (Encounter) model.getValueAt(selectedRowIndex, 3);

        if(e.getTreatment().getLaboratoryList().get(0).getLabTest().equals("Chemo Theorapy"))
        {
            txtLabAdminAmountToPay.setText("2000");
        }

        if(e.getTreatment().getLaboratoryList().get(0).getLabTest().equals("Radiation Theoray"))
        {
            txtLabAdminAmountToPay.setText("3000");
        }

        if(e.getTreatment().getLaboratoryList().get(0).getLabTest().equals("Surgery"))
        {
            txtLabAdminAmountToPay.setText("4000");
        }

    }//GEN-LAST:event_jButton1ActionPerformed


