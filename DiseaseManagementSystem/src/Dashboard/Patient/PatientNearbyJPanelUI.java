    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int selectedRowIndex = tblPatientHospital.getSelectedRow();

        if(selectedRowIndex < 0)
        {
            JOptionPane.showMessageDialog(this, "Please Select a row to view");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblPatientHospital.getModel();
        Hospital h = (Hospital) model.getValueAt(selectedRowIndex, 0);
        populatePatientDoctorTable(h.getHospitalName());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int selectedRowIndex = tblPatientDoctor.getSelectedRow();

        if(selectedRowIndex < 0)
        {
            JOptionPane.showMessageDialog(this, "Please Select a row to view");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblPatientDoctor.getModel();
        Doctor d = (Doctor) model.getValueAt(selectedRowIndex, 0);

        d.getDocPatList().add(patient);
        patient.getDoctors().add(d);

        JOptionPane.showMessageDialog(this, "Appointment scheduled");
    }//GEN-LAST:event_jButton3ActionPerformed

