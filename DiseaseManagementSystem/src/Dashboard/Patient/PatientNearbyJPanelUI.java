    private void populatePatientDoctorTable(String hospitalName) {
        DefaultTableModel model = (DefaultTableModel) tblPatientDoctor.getModel();
                  
                model.setRowCount(0);
                
                for(Doctor d : ecosystem.getDoctorList().getDoctorList())
                    
                {
                    
                    if(d.getHospital().getHospitalName().equals(hospitalName))
                    {
                        
                        
                    Object[] row = new Object[2];
                    row[0]=d;
                    row[1]=d.getQualification();
                    
                    
                    
                    
                    model.addRow(row);
                    
                    
                }
    
                }
       
       
    }

    private void populatePatientHospitalTable(String city) {
//        throw new UnsupportedOperationException("Not supported yet."); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
                
                DefaultTableModel model = (DefaultTableModel) tblPatientHospital.getModel();
                  
                model.setRowCount(0);
                
                for(Hospital h: ecosystem.getHospitalList().getHospitalList())
                    
                {
                    
                    if(h.getCity().getCityName().equals(city))
                    {   
                    Object[] row = new Object[2];
                    row[0]=h;
                    row[1]=h.getCity().getCityName();
                    model.addRow(row);
                }
                }
    }

