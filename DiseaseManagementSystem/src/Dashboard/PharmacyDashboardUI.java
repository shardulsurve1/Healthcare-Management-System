    private void populatePharmacyDashboardTable() {
      DefaultTableModel model = (DefaultTableModel) tblPharAdminPatient.getModel();
               
                  
                model.setRowCount(0);
        
        for(Patient p : ecosystem.getPatientList().getPatientList())
        {
           for(Encounter e : p.getEncounterHistory())
           {
                Object[] row = new Object[2];
                row[0]=p;
                row[1]=e.getTreatment();
                model.addRow(row);
           }
            
        }
    }
