    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int selectedRowIndex = tblPharAdminPatient.getSelectedRow();

        if(selectedRowIndex < 0)
        {
            JOptionPane.showMessageDialog(this, "Please Select a row of Encounter table");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblPharAdminPatient.getModel();
        Treatment t = (Treatment) model.getValueAt(selectedRowIndex, 1);

        //       ----------------------------------------------------------------------------------

        DefaultTableModel model1 = (DefaultTableModel) tblPharPatMedicineList.getModel();
        model1.setRowCount(0);

        int amountToPay = 0;

        for(Medicine m : t.getMedicineList() )
        {

            amountToPay+=10*m.getQuantity();

            Object[] row1 = new Object[3];
            row1[0]=m.getMedicine();
            row1[1]=m.getQuantity();

            model1.addRow(row1);

        }

        txtAmountTobePaid.setText(String.valueOf(amountToPay));
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int selectedRowIndex = tblPharAdminPatient.getSelectedRow();

        if(selectedRowIndex < 0)
        {
            JOptionPane.showMessageDialog(this, "Please Select a row of Encounter table");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblPharAdminPatient.getModel();
        Treatment t = (Treatment) model.getValueAt(selectedRowIndex, 1);
        Patient p = (Patient) model.getValueAt(selectedRowIndex, 0);

        if(p.getInsuranceRequest().equals("Approved") && (p.getCoverAmount()>= Integer.parseInt(txtAmountTobePaid.getText())))
        {
            if(p.getCoverAmount()>= Integer.parseInt(txtAmountTobePaid.getText()))
            {

                if(t.getMedicineStatus().equals("Pending"))
                {
                    p.setCoverAmount(p.getCoverAmount()- Integer.parseInt(txtAmountTobePaid.getText()));
                    t.setMedicineStatus("Completed");
                    JOptionPane.showMessageDialog(this, "Medicine Request is Completed");

                }

                else{

                    JOptionPane.showMessageDialog(this, "Medicine Request is Already Complete");

                }

            }

        }

        else if((p.getAccountBal() >= Integer.parseInt(txtAmountTobePaid.getText())))
        {
            if(t.getMedicineStatus().equals("Pending"))
            {
                p.setAccountBal(p.getAccountBal()- Integer.parseInt(txtAmountTobePaid.getText()));
                t.setMedicineStatus("Completed");
                JOptionPane.showMessageDialog(this, "Medicine Request is Completed");

            }

            else{

                JOptionPane.showMessageDialog(this, "Medicine Request is Already Complete");

            }

        }

        else {
            JOptionPane.showMessageDialog(this, "Patient doesn't have enough balance in account.");

        }

        populatePharmacyDashboardTable();

    }//GEN-LAST:event_jButton2ActionPerformed

